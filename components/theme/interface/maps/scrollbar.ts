export interface ScrollBarToken {
  /**
   * @desc 滚动条颜色
   * @descEN Scrollbar color
   * @default auto
   * @since 5.16.0
   */
  scrollbarColor: string;
  /**
   * @desc 滚动条宽度
   * @descEN Scrollbar width
   * @default auto
   * @since 5.16.0
   */
  scrollbarWidth: string;
  /**
   * @desc 设置滚动条出现的时候内容不晃动
   * @descEN Set the content not to shake when the scrollbar appears
   * @default auto
   * @since 5.16.0
   */
  scrollbarGutter: string;
}
